package Tartarus1;

public class PixelInfo {

    public int xCoord;
    public int yCoord;
    public int northColor;
    public int westColor;
    public int northWestColor;

    public PixelInfo(int xCoord, int yCoord, int northColor, int westColor, int northWestColor) {
        this.xCoord = xCoord;
        this.yCoord = yCoord;
        this.northColor = northColor;
        this.westColor = westColor;
        this.northWestColor = northWestColor;
    }
}
